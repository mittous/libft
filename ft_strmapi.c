#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*tmp;

	if (s && f)
	{
		tmp = malloc((ft_strlen(s) + 1) * sizeof(char));
		if (!tmp)
			return (NULL);
		i = 0;
		while (s[i])
		{
			tmp[i] = f(i, s[i]);
			i++;
		}
		tmp[i] = '\0';
		return (tmp);
	}
	return (NULL);
}
